import { readdir, readFile, writeFile } from "node:fs/promises";
import { resolve, join, relative } from "node:path";

const distDir = resolve("packages/studio/dist");
const outputFile = resolve("packages/studio/src/api/embedded-assets.generated.ts");

const contentTypes = {
  ".js": "application/javascript",
  ".css": "text/css",
  ".svg": "image/svg+xml",
  ".png": "image/png",
  ".ico": "image/x-icon",
  ".json": "application/json",
  ".html": "text/html; charset=utf-8",
  ".woff2": "font/woff2",
  ".webmanifest": "application/manifest+json",
};

function getContentType(filePath) {
  const entry = Object.entries(contentTypes).find(([ext]) => filePath.endsWith(ext));
  return entry?.[1] ?? "application/octet-stream";
}

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await walk(fullPath));
    } else {
      files.push(fullPath);
    }
  }
  return files;
}

const allFiles = await walk(distDir);
const fileEntries = [];
let embeddedIndexHtml = null;

for (const filePath of allFiles) {
  const rel = relative(distDir, filePath).replace(/\\/g, "/");
  const raw = await readFile(filePath);
  const base64 = raw.toString("base64");
  if (rel === "index.html") {
    embeddedIndexHtml = raw.toString("utf-8");
    continue;
  }
  fileEntries.push({ rel, base64, contentType: getContentType(rel) });
}

const generated = `/* eslint-disable */
// Auto-generated by scripts/generate-embedded-assets.mjs

const decodeBase64 = (value: string): Uint8Array => new Uint8Array(Buffer.from(value, "base64"));

export const embeddedIndexHtml = ${JSON.stringify(embeddedIndexHtml)};

export const embeddedFiles = {
${fileEntries.map((entry) => `  ${JSON.stringify(entry.rel)}: { content: decodeBase64(${JSON.stringify(entry.base64)}), contentType: ${JSON.stringify(entry.contentType)} },`).join("\n")}
} as const;
`;

await writeFile(outputFile, generated, "utf-8");
console.log(`Generated embedded assets: ${outputFile}`);
